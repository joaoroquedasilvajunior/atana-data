"""Build the Atana harmonisation crosswalk — canonical.domain_crosswalk.

Phase 3 (Part C) of the Atana Data LATAM expansion. This materialises, as a
real queryable Parquet table, the domain crosswalk that until now lived only as
per-country mapping tables inside the four methodology docs.

WHAT THIS IS
------------
A single reference table that maps every national cultural-statistics
classification in the corpus — Mexico's INEGI CSCM functional areas, Colombia's
DANE CSECC product cuadros, Argentina's SInCA CSC segments, Costa Rica's CSCCR
sectors — plus the two international classifications already in the corpus
(UNCTAD CER creative-goods / creative-services codes, IBGE NCM chapters) onto a
common spine: the 14 domains of the 2025 UNESCO Framework for Cultural
Statistics (FCS) — 7 cultural + 7 transversal.

It lets a cross-country query resolve through ONE table while keeping the
definitional gaps VISIBLE — the `mapping_confidence` and `notes` columns record
exactly where a map is exact, good, approximate, or has no equivalent. Nothing
is silently reconciled (the methodological-pluralism principle — cf. Atana
Note #03). Three gaps are flagged with ★ in `notes` — they are findings, not
noise: Mexican artesanías, digital-content straddle, advertising/architecture.

The crosswalk is NOT raw source data — it is a curated artifact, regenerated by
this script from the inline data below. It therefore lives in the `canonical`
schema / `curated/` folder, un-timestamped: it is a living reference table, not
a versioned analytical snapshot.

SOURCES OF THE MAPPINGS (authoritative — the inline data is transcribed from):
    docs/methodology/inegi_csc.md             §5   — 10 CSCM áreas generales
    docs/methodology/dane_csecc.md            §4   — 22 CSECC trade cuadros
    docs/methodology/sinca_csc.md             §5   — 2 SInCA CSC segments
    docs/methodology/cr_bccr_csc.md           §5   — 4 CSCCR sectors
    docs/methodology/ibge_estruturais_siic_ch2.md  — SIIC cultural domains (Phase 4)
    docs/methodology/ibge_tic_siic_ch7.md          — SIIC participation proxy (Phase 4)
    _atana_intel/phase3_schema_design.md      §C.4 — table design + the FCS spine
    _atana_intel/phase4_scoping.md            §E   — the Phase 4 crosswalk extension

Output:
    curated/domain_crosswalk.parquet     (+ .meta.json)

Idempotent: inline data → DuckDB COPY to Parquet (no pyarrow); byte-identical
reruns. MotherDuck push gated behind a token (env var or .motherduck_token
file). No file in raw/ is read, moved or modified.

Usage:
    python etl/canonical__build_domain_crosswalk.py
"""
