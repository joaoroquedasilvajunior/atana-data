def validate(df: pd.DataFrame) -> None:
    """Self-check the crosswalk before it is written."""
    print("Validating...")
    expect = {"fcs2025": 14, "inegi": 10, "dane": 22, "sinca": 2,
              "cr_bccr": 4, "unctad": 15, "ibge_comex": 5, "ibge_siic": 10,
              "bcb": 1, "inpi": 1, "ecad": 1}
    got = df["source_schema"].value_counts().to_dict()
    for schema, n in expect.items():
        assert got.get(schema) == n, (
            f"{schema}: expected {n} rows, got {got.get(schema)}")
    total = sum(expect.values())
    assert len(df) == total == 85, f"total rows {len(df)} != 85"
    print(f"  ✓ {len(df)} rows — " + ", ".join(f"{k} {v}" for k, v in expect.items()))

    bad = set(df["mapping_confidence"]) - CONFIDENCE_VALUES
    assert not bad, f"unexpected mapping_confidence values: {bad}"
    print(f"  ✓ mapping_confidence ∈ {sorted(CONFIDENCE_VALUES)}")

    # Every "clean" (single, non-bundle, non-null) fcs2025_domain must be one
    # of the 14 spine labels. Bundles (containing ';' or '/') and 'Multiple …'
    # rows and NULLs are the documented many-to-many / no-equivalent cases.
    clean_bad = []
    for d in df["fcs2025_domain"].dropna().unique():
        if ";" in d or "/" in d or d.startswith("Multiple"):
            continue
        if d not in FCS_DOMAIN_LABELS:
            clean_bad.append(d)
    assert not clean_bad, f"fcs2025_domain off-spine: {clean_bad}"
    print(f"  ✓ every clean fcs2025_domain is one of the 14 FCS spine domains")

    # No-equivalent rows must have a NULL domain; non-no-equivalent must not.
    ne = df["mapping_confidence"] == "no-equivalent"
    assert df.loc[ne, "fcs2025_domain"].isna().all(), \
        "a 'no-equivalent' row carries a domain"
    assert df.loc[~ne, "fcs2025_domain"].notna().all(), \
        "a non-'no-equivalent' row has a NULL domain"
    print(f"  ✓ NULL fcs2025_domain ⇔ mapping_confidence = 'no-equivalent' "
          f"({int(ne.sum())} rows)")

    # Coverage: which FCS spine domains are reached by at least one corpus
    # cultural-statistics source row — the LATAM national CSC schemas plus, from
    # Phase 4, the IBGE SIIC cultural-domain classification. This is the live
    # 'N/14 domains reached' progress meter for the transversal-blind-spot work.
    nat = df[df["source_schema"].isin(
        ["inegi", "dane", "sinca", "cr_bccr", "ibge_siic", "bcb", "inpi",
         "ecad"])]
    reached = set()
    for d in nat["fcs2025_domain"].dropna():
        for part in str(d).replace(";", "/").split("/"):
            p = part.strip()
            if p in FCS_DOMAIN_LABELS:
                reached.add(p)
    unreached = sorted(FCS_DOMAIN_LABELS - reached)
    print(f"  · {len(reached)}/14 FCS domains reached by a national CSC, "
          f"IBGE SIIC, BCB, INPI or ECAD row; not reached: {unreached or '—'}")
    conf = df["mapping_confidence"].value_counts().to_dict()
    print(f"  · confidence mix: " + ", ".join(
        f"{k} {conf.get(k, 0)}" for k in
        ["exact", "good", "approximate", "no-equivalent"]))


def write_parquet(df: pd.DataFrame) -> Path:
    out_path = OUT / "domain_crosswalk.parquet"
    con = duckdb.connect()
    con.register("df_data", df)
    con.execute(
        f"COPY df_data TO '{out_path}' (FORMAT PARQUET, COMPRESSION ZSTD)")
    size_kb = out_path.stat().st_size / 1024
    print(f"  ✓ {out_path.relative_to(REPO_ROOT)} — {len(df):,} rows, "
          f"{size_kb:.1f} KB")
    return out_path


